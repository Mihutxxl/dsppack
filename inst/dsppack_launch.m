    function cb_preset_wrapper(src, ~)
        idx = get(src, "value");
        if idx <= 1; return; end
        handles = guidata(src);

        %                   Fs      Fc     Fc2    N  type topo arch
        defs = {
            44100,  20000,  0,     8, 1, 1, 1;   % Anti-aliasing (CD Audio)
            8000,   300,    3400,  4, 3, 1, 1;   % Voice Band-pass
            44100,  250,    0,     4, 1, 2, 1;   % Bass Isolation
            44100,  4000,   0,     4, 2, 1, 1;   % Treble Isolation
            44100,  80,     0,     4, 1, 1, 1;   % Sub-bass Crossover
            1000,   45,     55,    4, 4, 1, 1;   % 50 Hz Mains Notch
            1000,   55,     65,    4, 4, 1, 1;   % 60 Hz Mains Notch
            44100,  10,     0,     2, 2, 1, 1;   % DC Blocker
            44100,  8000,   0,     6, 1, 3, 1;   % Wideband Noise Reduction
            44100,  20000,  0,    64, 1, 1, 2;   % FIR Anti-aliasing (CD Audio)
            8000,   300,    3400, 48, 3, 1, 2;   % FIR Voice Band-pass
            44100,  4000,   0,    48, 1, 3, 2;   % FIR Noise Suppression
        };

        d = defs(idx - 1, :);
        set(handles.edit_Fs,    "string", num2str(d{1}));
        set(handles.edit_Fc,    "string", num2str(d{2}));
        set(handles.edit_Fc2,   "string", num2str(d{3}));
        set(handles.edit_Order, "string", num2str(d{4}));
        set(handles.popup_Type, "value",  d{5});
        set(handles.popup_Arch, "value",  d{7});
        if d{7} == 1
            set(handles.popup_Topo, "String", ...
                {"Butterworth", "Chebyshev I", "Chebyshev II", "Elliptic"});
        else
            set(handles.popup_Topo, "String", ...
                {"Hamming Window", "Hanning Window", "Blackman Window", ...
                 "Kaiser Window", "Parks-McClellan", "Least-Squares"});
        end
        set(handles.popup_Topo, "value",  d{6});

        dsp_design_callback(handles);
    end

    function cb_arch_wrapper(src, ~)
        arch_idx = get(src, "value");
        handles = guidata(src);
        if arch_idx == 1
