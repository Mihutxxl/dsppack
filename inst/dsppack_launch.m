    function cb_analyze_wrapper(src, ~)
        handles = guidata(src);
        Fs = str2double(get(handles.edit_Fs, "string"));

        if isnan(Fs) || Fs <= 0
            errordlg("Please enter a valid Sampling Frequency (Fs)."); return;
        end

        % Use the coefficients from the last design action (DESIGN FILTER
        % button or P/Z editor submit). Fall back to recomputing from the
        % UI controls only if no filter has been designed yet.
        if isfield(handles, "last_b") && ~isempty(handles.last_b)
            b = handles.last_b;
            a = handles.last_a;
        else
            Fc       = str2double(get(handles.edit_Fc,    "string"));
            Fc2      = str2double(get(handles.edit_Fc2,   "string"));
            N        = str2double(get(handles.edit_Order, "string"));
            type_idx = get(handles.popup_Type, "value");
            topo_idx = get(handles.popup_Topo, "value");

            if isnan(Fc) || isnan(N)
                errordlg("Please design a filter first."); return;
            end
            if Fc >= Fs/2
                errordlg("Cutoff Fc must be less than Nyquist (Fs/2)."); return;
            end
            if type_idx >= 3 && (isnan(Fc2) || Fc2 >= Fs/2 || Fc2 <= Fc)
                errordlg("For Band Pass/Stop, Fc2 must satisfy: Fc < Fc2 < Fs/2."); return;
            end

            arch_idx = get(handles.popup_Arch, "value");
            Rp = str2double(get(handles.edit_Rp, "string"));
            Rs = str2double(get(handles.edit_Rs, "string"));
            if isnan(Rp) || Rp <= 0; Rp = 3; end
            if isnan(Rs) || Rs <= 0; Rs = 40; end
            try
                [b, a] = dsp_compute_filter(Fs, Fc, Fc2, N, type_idx, topo_idx, arch_idx, Rp, Rs);
            catch err
                errordlg(sprintf("Filter design failed:\n%s", err.message), "Design Error");
                return;
            end
        end

        current_fig = ancestor(src, "figure");
        set(current_fig, "Visible", "off");
        dsp_result_window(current_fig, b, a, Fs);
    end

    function cb_pz_editor_wrapper(src, ~)
