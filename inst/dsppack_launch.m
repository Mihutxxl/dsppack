            errordlg("Please enter a valid Sampling Frequency (Fs) first."); return;
        end
        current_fig = ancestor(src, "figure");
        set(current_fig, "Visible", "off");
        fda_pz_editor(current_fig, Fs);
    end

    function cb_audio_demo_wrapper(src, ~)
        handles = guidata(src);
        Fs = str2double(get(handles.edit_Fs, "string"));

        if isnan(Fs) || Fs <= 0
            errordlg("Please enter a valid Sampling Frequency (Fs)."); return;
        end

        if ~isfield(handles, "last_b") || isempty(handles.last_b)
            errordlg("Please design a filter first."); return;
        end

        current_fig = ancestor(src, "figure");
        set(current_fig, "Visible", "off");
        dsp_audio_demo(current_fig, handles.last_b, handles.last_a, Fs);
    end

    function cb_exit_wrapper(~, ~)
        choice = questdlg("Are you sure you want to exit?", ...
                          "Exit Tool", "Yes", "No", "No");
        if strcmp(choice, "Yes")
            % Close only this tool's own windows (tagged by dsp_new_window),
            % leaving any unrelated Octave figures untouched.
            delete(findobj(0, "type", "figure", "tag", "dsppack_window"));
        end
    end
